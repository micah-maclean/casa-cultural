package com.casaCultural.Atividade1.controller.model;
public class Analise {
    int id;
    int filmeId;
    String analise;
    int nota;
    
    public int getID() { return this.id; }
    public void setId(int id) { this.id = id; }
    
    public int getFilmeId() { return this.filmeId; }
    public void setFilmeId(int filmeId) { this.filmeId = filmeId; }
    
    public String getAnalise() {return this.analise;}
    public void setAnalise(String analise) { this.analise = analise; }
    
    
    public int getNota() { return nota; }
    public void setNota(int nota) { this.nota = nota; }
    
}
